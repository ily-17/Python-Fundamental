# Integer

age = 25

# Float

salary = 75000.50

# String

name = "Shirley"

# Boolean

is_active = True

# List

skills = ["Python", "Java", "SQL"]

# Tuple

coordinates = (10, 20)

# Set

numbers = {1, 2, 3, 4}

# Dictionary

employee = {
    "name": "Shirley",
    "role": "AI Engineer"
}

