# Arithmetic Operators

a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison Operators

print("\nComparison Operators")

print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# Logical Operators

age = 25
salary = 50000

print("\nLogical Operators")

print(age > 18 and salary > 30000)
print(age > 30 or salary > 30000)
print(not age < 18)

# Assignment Operators

x = 10

x += 5
print("\nAfter += :", x)

x -= 2
print("After -= :", x)

x *= 3
print("After *= :", x)

# String Operations

first_name = "Shirley"
last_name = "Deborah"

print("\nString Concatenation:")
print(first_name + " " + last_name)

print("\nString Repetition:")
print("Python " * 3)
